// C program to find largest among two
// numbers using ternary operator
 
#include <stdio.h>
void __start()
{
	int a=main();
	_Exit(0);
}
 
int main()
{
    int m = 5, n = 4;
 
    (m > n) ? printf("m is greater than n that is %d > %d",
                     m, n)
            : printf("n is greater than m that is %d > %d",
                     n, m);
 
    return 0;
}

